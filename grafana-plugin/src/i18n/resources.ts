export async function loadPluginResources(language: string): Promise<Resources> {
  if (language === CHINESE_SIMPLIFIED) {
    return zhHansResources;
  }
  return {};
}

export function hasPluginResources(language: string): boolean {
  return language === DEFAULT_LANGUAGE || language === CHINESE_SIMPLIFIED;
}
